using System;

namespace Test {
    // Класс "Стол" - производный от "Мебель"
    class Stol : Mebel {
        private double длина;
        private double ширина;
        private double высота;

        // Конструктор с вызовом базового конструктора
        public Stol(string название, double цена, Material материал, 
                   double длина, double ширина, double высота) 
            : base(название, цена, материал) {
            this.длина = длина;
            this.ширина = ширина;
            this.высота = высота;
        }

        // Переопределенное свойство
        public override string ПолнаяИнформация {
            get {
                // Вызов базового свойства через base
                return base.ПолнаяИнформация + 
                       $", Размеры: {длина}×{ширина}×{высота} см";
            }
        }

        // Переопределенный метод
        public override void Info() {
            // Вызов базового метода через base
            base.Info();
            Console.WriteLine($"Размеры: {длина}×{ширина}×{высота} см");
            Console.WriteLine($"Площадь столешницы: {РассчитатьПлощадь():F2} см²");
            Console.WriteLine();
        }

        // Переопределенный метод расчета стоимости (с учетом площади)
        public override double РассчитатьСтоимость() {
            // Вызов базового метода
            double базоваяСтоимость = base.РассчитатьСтоимость();
            double площадь = РассчитатьПлощадь();
            // Дополнительная стоимость за площадь
            return базоваяСтоимость + площадь * 0.1;
        }

        // Переопределенный метод получения типа
        public override string ПолучитьТип() {
            return "Стол";
        }

        // Специфичный метод для стола
        private double РассчитатьПлощадь() {
            return длина * ширина;
        }
    }
}

