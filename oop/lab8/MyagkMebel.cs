using System;

namespace Test {
    // Класс "Мягкая мебель" - производный от "Мебель"
    class MyagkMebel : Mebel {
        private int количество_мест;
        private bool имеет_спинку;

        // Конструктор с вызовом базового конструктора
        public MyagkMebel(string название, double цена, Material материал,
                         int количество_мест, bool имеет_спинку)
            : base(название, цена, материал) {
            this.количество_мест = количество_мест;
            this.имеет_спинку = имеет_спинку;
        }

        // Переопределенное свойство
        public override string ПолнаяИнформация {
            get {
                // Вызов базового свойства
                string спинка = имеет_спинку ? "со спинкой" : "без спинки";
                return base.ПолнаяИнформация + 
                       $", Мест: {количество_мест}, {спинка}";
            }
        }

        // Переопределенный метод
        public override void Info() {
            // Вызов базового метода
            base.Info();
            Console.WriteLine($"Количество мест: {количество_мест}");
            Console.WriteLine($"Спинка: {(имеет_спинку ? "Да" : "Нет")}");
            Console.WriteLine($"Комфортность: {РассчитатьКомфортность():F2}");
            Console.WriteLine();
        }

        // Переопределенный метод расчета стоимости
        public override double РассчитатьСтоимость() {
            // Вызов базового метода
            double базоваяСтоимость = base.РассчитатьСтоимость();
            // Дополнительная стоимость за комфорт
            double доплата = количество_мест * 500;
            if (имеет_спинку) доплата += 1000;
            return базоваяСтоимость + доплата;
        }

        // Переопределенный метод получения типа
        public override string ПолучитьТип() {
            return "Мягкая мебель";
        }

        // Специфичный метод для мягкой мебели
        private double РассчитатьКомфортность() {
            double комфорт = количество_мест * 10;
            if (имеет_спинку) комфорт += 5;
            if (материал == Material.Кожа) комфорт += 10;
            return комфорт;
        }
    }
}

