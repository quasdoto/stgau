using System;

namespace Test {
    // Класс "Шкаф" - производный от "Мебель"
    class Shkaf : Mebel {
        private double ширина;
        private double высота;
        private double глубина;
        private int количество_дверей;

        // Конструктор с вызовом базового конструктора
        public Shkaf(string название, double цена, Material материал,
                    double ширина, double высота, double глубина, int количество_дверей)
            : base(название, цена, материал) {
            this.ширина = ширина;
            this.высота = высота;
            this.глубина = глубина;
            this.количество_дверей = количество_дверей;
        }

        // Переопределенное свойство
        public override string ПолнаяИнформация {
            get {
                // Вызов базового свойства
                return base.ПолнаяИнформация + 
                       $", Размеры: {ширина}×{высота}×{глубина} см, Дверей: {количество_дверей}";
            }
        }

        // Переопределенный метод
        public override void Info() {
            // Вызов базового метода
            base.Info();
            Console.WriteLine($"Размеры: {ширина}×{высота}×{глубина} см");
            Console.WriteLine($"Количество дверей: {количество_дверей}");
            Console.WriteLine($"Объем: {РассчитатьОбъем():F2} л");
            Console.WriteLine();
        }

        // Переопределенный метод расчета стоимости
        public override double РассчитатьСтоимость() {
            // Вызов базового метода
            double базоваяСтоимость = base.РассчитатьСтоимость();
            double объем = РассчитатьОбъем();
            // Дополнительная стоимость за объем и двери
            return базоваяСтоимость + объем * 0.5 + количество_дверей * 800;
        }

        // Переопределенный метод получения типа
        public override string ПолучитьТип() {
            return "Шкаф";
        }

        // Специфичный метод для шкафа
        private double РассчитатьОбъем() {
            // Объем в литрах (1 литр = 1000 см³)
            return (ширина * высота * глубина) / 1000.0;
        }
    }
}

